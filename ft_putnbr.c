#include "libft.h"

void	ft_putnbr(int n)
{
	char		c;

	if (n < 0)
	{
		ft_putchar('-');
		if (n == -2147483648)
		{
			ft_putchar('2');
			n *= -1;
			n = n % 1000000000;
		}
		n *= -1;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	c = (n % 10) + '0';
	ft_putchar(c);
}
